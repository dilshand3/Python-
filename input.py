operator = input("enter a mode from '+,-,*,/' : ")
a = int(input("enter a value : "))
b = int(input("enter b value : "))

if(operator == '+'):
  print(a + b)
elif(operator == '-'):
  print(a-b)
elif(operator == '*'):
  print(a*b)
elif(operator == '/'):
  print(a/b)


